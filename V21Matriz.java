public class V21Matriz{

    public static void main(String args[]){

        int Nums[][]= new int [2][2];

        Nums[0][0] = 5;
        Nums[0][1] = 2;
        Nums[1][0] = 2;
        Nums[1][1] = 5;

        System.out.print("["+ Nums[0][0] +"]");
        System.out.println("["+ Nums[0][1] +"]");
        System.out.print("["+ Nums[1][0] +"]");
        System.out.println("["+ Nums[1][1] +"]");
    }
}